package fastinfoset;

/**
 *
 * @author rpablos
 */
public class FastInfosetException extends Exception {

    public FastInfosetException() {
    }

    public FastInfosetException(String message) {
        super(message);
    }

    public FastInfosetException(Throwable cause) {
        super(cause);
    }

    public FastInfosetException(String message, Throwable cause) {
        super(message, cause);
    }
    
}
