public class EncodingAlgorithmException extends fastinfoset.FastInfosetException {

    public EncodingAlgorithmException() {
    }

    public EncodingAlgorithmException(String message) {
        super(message);
    }

    public EncodingAlgorithmException(Throwable cause) {
        super(cause);
    }

    public EncodingAlgorithmException(String message, Throwable cause) {
        super(message, cause);
    }
    
}
